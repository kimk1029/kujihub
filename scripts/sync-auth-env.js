const fs = require('fs');
const path = require('path');
const { getAppEnvCandidates, mergeEnvFiles } = require('./env-paths');

const projectRoot = path.resolve(__dirname, '..');
const outputDir = path.join(projectRoot, 'src', 'generated');
const outputPath = path.join(outputDir, 'authEnv.js');

function pick(env, key, fallback = '') {
  const value = process.env[key] ?? env[key] ?? fallback;
  return typeof value === 'string' ? value : String(value);
}

const parsedEnv = mergeEnvFiles(getAppEnvCandidates());

const authEnv = {
  GOOGLE_WEB_CLIENT_ID: pick(parsedEnv, 'GOOGLE_WEB_CLIENT_ID'),
  GOOGLE_IOS_CLIENT_ID: pick(parsedEnv, 'GOOGLE_IOS_CLIENT_ID'),
  KAKAO_NATIVE_APP_KEY: pick(parsedEnv, 'KAKAO_NATIVE_APP_KEY'),
  NAVER_CLIENT_ID: pick(parsedEnv, 'NAVER_CLIENT_ID'),
  NAVER_CLIENT_SECRET: pick(parsedEnv, 'NAVER_CLIENT_SECRET'),
  NAVER_CALLBACK_SCHEME: pick(parsedEnv, 'NAVER_CALLBACK_SCHEME', 'kujihub'),
  NAVER_CALLBACK_HOST: pick(parsedEnv, 'NAVER_CALLBACK_HOST', 'auth'),
  NAVER_CALLBACK_PATH: pick(parsedEnv, 'NAVER_CALLBACK_PATH', '/naver/callback'),
};

const fileContents = `/* eslint-disable */
// Auto-generated by scripts/sync-auth-env.js
module.exports.AUTH_ENV = ${JSON.stringify(authEnv, null, 2)};
`;

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputPath, fileContents, 'utf8');

console.log(`Synced auth env to ${path.relative(projectRoot, outputPath)}`);
